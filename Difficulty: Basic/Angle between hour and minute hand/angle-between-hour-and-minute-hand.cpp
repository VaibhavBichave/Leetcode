class Solution {
  public:
    int getAngle(int H , int M) {
        H = ((H - 12) + 12)% 12;
        float angle = abs(0.5 * (H * 60 - 11 * M)); 
        angle = min(360 - angle, angle); 
        return angle; 
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int H,M;
        
        cin>>H>>M;

        Solution ob;
        cout << ob.getAngle(H,M) << endl;
    }
    return 0;
}
