// User function template for C++
class Solution {
  public:
    void segregateEvenOdd(vector<int>& arr) {
        for(int &x: arr) if(x&1) x+=1e7;
        sort(arr.begin(), arr.end());
        for(int &x: arr) if(x&1) x-=1e7;
    }
};

//{ Driver Code Starts.

int main() {
    string ts;
    getline(cin, ts);
    int t = stoi(ts);
    while (t--) {
        vector<int> arr;
        string input;
        getline(cin, input);
        stringstream ss(input);
        int number;
        while (ss >> number) {
            arr.push_back(number);
        }
        Solution obj;
        obj.segregateEvenOdd(arr);
        for (int i = 0; i < arr.size(); i++) {
            cout << arr[i] << " ";
        }
        cout << endl;
    }
    return 0;
}

