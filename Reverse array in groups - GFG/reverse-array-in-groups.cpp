class Solution{
public:
    //Function to reverse every sub-array group of size k.
    void reverseInGroups(vector<long long>& arr, int n, int k){
        int start = 0, end = min(n-1,k-1);
        while(start<n){
            int i=start, j=end;
            while(i<j) swap(arr[i++], arr[j--]);
            start = end+1;
            end = min(n-1,start+k-1);
        }
    }
};

